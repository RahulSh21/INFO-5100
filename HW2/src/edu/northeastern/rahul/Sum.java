package edu.northeastern.rahul;

public class Sum {
    int add(int a, int b){
        return a + b;
    }
    int add(int a, int b, int c){
        return a + b + c;
    }
    double add(int a, double b){
        return a + b;
    }
    double add(double a, int b){
        return a + b;
    }
    double add(double a, double b){
        return a + b;
    }
}
